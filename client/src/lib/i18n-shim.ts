function mockT(key: string, params?: Record<string, any>): string {
  let text = AR_TRANSLATIONS[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{{${k}}}`, String(v ?? ""));
    });
  }
  return text;
}

